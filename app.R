makeBlastQuery <- function(bln_query_input){
  if (startsWith(bln_query_input, ">")) {
    bln_query_id <- gsub(">", "", gsub("\n.+", "", bln_query_input))
    bln_query_id <- gsub("[() :]", "", bln_query_id)
    bln_query_id <- paste0(">", bln_query_id, "\n")
    
    bln_query_seq <- gsub(".*>.+?\n", "", bln_query_input)
    bln_query_seq <- gsub("\n", "", bln_query_seq)
  }else{
    bln_query_id <- ">Query\n"
    bln_query_seq <- gsub("\n", "", bln_query_input)
  }
  return(c(bln_query_id, bln_query_seq))
}
 
