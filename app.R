doSeqLogo <- function( intmp4seqlogo, outtmp4seqlogo ){
  png( outtmp4seqlogo, width=1000, height=300 ) 
  in_f <- intmp4seqlogo
  fasta4seqlogo <- readDNAStringSet(in_f, format="fasta")
  consm4seqlogo <- consensusMatrix(fasta4seqlogo, as.prob=T, baseOnly =T )
  pwm4seqlogo   <- makePWM( consm4seqlogo[1:4, ])
  seqLogo( pwm4seqlogo )
  dev.off()
}

doPfix <- function( u, g, s, Ne ){
    i   <- 1
    obj <- matrix(nrow=20, ncol=2)
    while ( i < 21 ){
      negs    <- (-1 * s)
      negs_ne <- ((-1 * s) * Ne )
      pfix <- round(( (1 - exp( negs )) / (1 - exp( negs_ne )) ), digits = 8)
      obj[i, 1] <-  as.numeric( s )
      obj[i, 2] <-  pfix
      s <- s + 0.001
      i <- i + 1
    }
  return( obj )
}

